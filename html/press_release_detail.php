            <div class="col-sm-6">
                <p>Today, a long established national veterans’ organization castigated the producers of Saturday Night
                    Live for its belittling portrayal of Jimmy Stewart in a rerun of a skit that originally was aired
                    twenty one years ago.</p>
                <p>“Jimmy Stewart was a highly decorated WWII bomber pilot. The belittling and disgusting treatment of
                    General Stewart as a buffoon on the Saturday Night broadcast is an affront to all American Veterans,
                    especially those who served in World War II,” said MAJ Brian Hampton USAR (ret) the President of the
                    nationwide Circle of Friends for American Veterans.</p>
                <h4>This country’s conscience is bigger than all the studios in Hollywood put together, and the time
                    will come when we’ll have to fight.</h4>
                <p>On Armed Forces Day, May 20th Saturday Night Live was not fully live, but chose to rebroadcast an
                original skit that ran on May 18, 1996. Jim Carrey portrayed Stewart and another actor portrayed Joe
                Pesci. During the skit, a hand signal for senility was directed
                to the audience about Stewart.</p>
                <p>Later, the script has Stewart reciting an inane poem, saying that he probably died six years before
                    and later being knocked to the floor with a baseball bat.
                    Stewart rallies at the end, but the damage was done, the impression left.</p>
                <p>The veterans’ group said that rebroadcasting of the skit was doubly reprehensible because it was in
                    terrible taste to begin with and broadcast on a day meant to celebrate military service. The
                    original skit was broadcast six months after Stewart was hospitalized and had a pacemaker
                    installed.</p>
            </div>
            <div class="col-sm-6">
                <p>Stewart was the first major American movie star to wear a military uniformduring World WII. He was
                    first turned down by both the Army and Air Corps for being underweight, until he succeeded to enlist
                    on his third try.</p>
                <p>MGM head Louis B. Mayer first tried to talk Stewart out of enlisting, claiming he would be
                    sacrificing his entire career. Stewart responded that, “This country’s
                    conscience is bigger than all the studios in Hollywood put together, and the time will come when
                    we’ll have to fight.”</p>
                <p>He later flew many combat missions deep into Nazi-occupied Europe. He was awarded the Distinguished
                    Flying Cross, the Air Force Distinguished Service Medal and the Air Medal with three Oak Leaf
                    Clusters among ther awards. He retired as a Brigadier General in the Air Force Reserve.</p>
                <p>Stewart rarely spoke about his military service. Many of his contemporaries like John Wayne did not
                    serve in the military, but starred in many military movies. Stewart
                    appeared in documentaries on military service, but eschewed war movie roles because of modesty and
                    that he had served the part in real life.</p>
                <p>The Circle of Friends for American Veterans has called on the producers of Saturday Night Live to
                    announce this Memorial Day that they have planned the airing of an episode that is entertaining but
                    also lauds patriotic military service.</p>
                <p>Since 1993, The Circle of Friends for American Veterans has harnessed the power of the media to rally
                    public support on behalf of American Veterans. COFAV has sponsored 196 programs and forums, with
                    well over 100 Members of Congress speaking at its events. The Circle of Friends for American
                    Veterans has supported local homeless veteran transitional facilities by organizing and conducting
                    programs for public support
                    in their local areas, as well as through earned media.</p>
